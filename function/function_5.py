#increment function

# (+)

def increment(number_1 ,number_2):
    return number_1+number_2
print(increment(3,6))

#(-)

def increment(number_1 ,number_2):
    return number_1-number_2
print(increment(3,6))

#(*)

def increment(number_1 ,number_2):
    return number_1*number_2
print(increment(3,6))

#(/)

def increment(number_1 ,number_2):
    return number_1/number_2
print(increment(3,6))

#(<)

def increment(number_1 ,number_2):
    return number_1<number_2
print(increment(3,6))

#(>)

def increment(number_1 ,number_2):
    return number_1>number_2
print(increment(3,6))

#(>=)

def increment(number_1 ,number_2):
    return number_1>=number_2
print(increment(3,6))

#<=

def increment(number_1 ,number_2):
    return number_1<=number_2
print(increment(3,6))